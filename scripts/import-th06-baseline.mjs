function renameIfExists(from, to) {
  if (!existsSync(from)) return;
  renameSync(from, to);
}

function walk(dir) {
  const out = [];
  for (const entry of readdirSync(dir)) {
    const path = join(dir, entry);
    if (statSync(path).isDirectory()) out.push(...walk(path));
    else out.push(path);
  }
  return out;
}

function replaceOne(text, pattern, replacement, label) {
  const next = text.replace(pattern, replacement);
  if (next === text) throw new Error(`Failed to patch ${label}`);
  return next;
}

function writeText(path, text) {
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, text);
}

function stringsByValue() {
  const map = new Map();
  for (const line of readFileSync(ghidraStrings, 'utf8').split(/\r?\n/).slice(1)) {
    if (!line.trim()) continue;
    const tab = line.indexOf('\t');
    if (tab < 0) continue;
    map.set(line.slice(tab + 1), line.slice(0, tab));
  }
  return map;
}

